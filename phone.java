package tes;

public class phone {
	private String phoneName;
	private int price;
	
	public void setPhoneName(String phoneName)
	{
		this.phoneName=phoneName;
	}
	
	public void setPrice(int price)
	{
		this.price=price;
	}
	
	public String getPhoneName()
	{
		return phoneName;
	}
	

	public int setPrice()
	{
		return price;
	}
	
}
